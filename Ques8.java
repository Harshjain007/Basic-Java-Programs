//8. program to find maximum between two numbers using switch case.

public class Ques8 {
	public static void main(String args[]) {
		int a=5, b=6;
		switch(a) {
			case 5 > 6: System.out.println(a + "is greater then " + b);
			default: System.out.println(b + "is greater then" + a);
		}
	}
}
